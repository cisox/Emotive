Emotive.emotesByLetter = {}

for i = 1, #Emotive.emotes do
    local firstCharacter = string.sub(Emotive.emotes[i][1], 1, 1)
    local key = string.upper(firstCharacter)

    if Emotive.emotesByLetter[key] == nil then
        Emotive.emotesByLetter[key] = {}
    end

    Emotive.emotesByLetter[key][Emotive.emotes[i][1]] = {
        ["Animation"] = Emotive.emotes[i][2],
        ["Voice"] = Emotive.emotes[i][3],
        ["Restrictions"] = Emotive.emotes[i][4],
    }
end
